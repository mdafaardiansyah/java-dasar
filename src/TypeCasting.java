/**
 * Provides utility methods for typecasting
 */

public class TypeCasting {

    /**
     * Converts a double-precision floating-point value to an integer.
     *
     * @param value The double value to be converted.
     * @return The resulting int value after truncation.
     */
    public int doubleToInteger(double value){
        return (int) value;
    }

    /**
     * Converts an integer value to a double.
     *
     * @param value The double value to be converted.
     * @return The resulting int value after truncation.
     */
    public double integerToDouble(int value){
        return (double) value;
    }
}
